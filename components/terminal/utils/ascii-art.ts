// Function to get random ASCII art
export function getRandomArt(): string {
  const artKeys = Object.keys(asciiArt) as (keyof typeof asciiArt)[];
  const randomKey = artKeys[Math.floor(Math.random() * artKeys.length)];
  return asciiArt[randomKey];
}

// Function to create animated text effect
export function createAnimatedText(text: string, effect: 'typewriter' | 'matrix' | 'glitch' = 'typewriter'): string {
  switch (effect) {
    case 'matrix':
      return `üü¢ ${text.split('').join(' ')}`;
    case 'glitch':
      return `‚ö° ${text.toUpperCase().split('').map(char => 
        Math.random() > 0.8 ? `${char}${String.fromCharCode(33 + Math.floor(Math.random() * 94))}` : char
      ).join('')}`;
    case 'typewriter':
    default:
      return `üìù ${text}`;
  }
}

// Function to create progress bar
export function createProgressBar(percentage: number, width: number = 20): string {
  const filled = Math.floor((percentage / 100) * width);
  const empty = width - filled;
  const bar = '‚ñà'.repeat(filled) + '‚ñë'.repeat(empty);
  return `[${bar}] ${percentage}%`;
}

// Function to create loading animation
export function createLoadingAnimation(step: number): string {
  const frames = ['‚†ã', '‚†ô', '‚†π', '‚†∏', '‚†º', '‚†¥', '‚†¶', '‚†ß', '‚†á', '‚†è'];
  return frames[step % frames.length];
}